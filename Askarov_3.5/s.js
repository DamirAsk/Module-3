let n_week = Number(prompt("Введи порядковый номер дня недели "))
switch (n_week) {
  case 1:
      console.log("Понедельник");
      console.log("1: 11111111");
      console.log("2: 2222222");
      console.log("3: 3333333");
      break;
  case 2:
      console.log("Вторник");
      console.log("1: 4444444");
      console.log("2: 555555");
      console.log("3: 66666666");
      break;
  case 3:
      console.log("Среда");
      console.log("1: 77777");
      console.log("2: 888888");
      console.log("3: 99999999");
      break;
  case 4:
      console.log("Четверг");
      console.log("1: 00000");
      console.log("2: йййййй");
      console.log("3: ццццц");
      break;
  case 5:
      console.log("Пятница");
      console.log("1: аааааааа");
      console.log("2: bbbbbbb");
      console.log("3: ccccccc");
      break;
  case 6:
      console.log("Суббота");
      console.log("1: уууууу");
      console.log("2: кккккк");
      console.log("3: ееееее");
      break;
  case 7:
      console.log("Воскресенье");
      console.log("1: нннннн");
      console.log("2: гггггг");
      console.log("3: шшшшшшшшшшшш");
      break;
  default:
      console.log("Не верные данные");
}